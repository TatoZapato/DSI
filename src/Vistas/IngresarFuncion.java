    private LinkedList<String> buscarCoeficientes() {
        String[] coeficientes = {"dap", "h"};
        LinkedList<String> co = new LinkedList();
        for (String coeficiente : coeficientes) {
            if (txtFuncion.getText().contains(coeficiente)) {
                co.add(coeficiente);
            }
        }
        return co;
    }

