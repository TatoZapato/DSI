    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        if (!txtFuncion.getText().equals("")) {
            if (txtFuncion.getText().length() >= 3) {
                String x = txtFuncion.getText().substring(txtFuncion.getText().length() - 3);
                String y = txtFuncion.getText().substring(0, txtFuncion.getText().length() - 3);
                if (x.equals("dap")) {
                    this.txtFuncion.setText(y);
                } else {
                    this.txtFuncion.setText(this.txtFuncion.getText().substring(0, txtFuncion.getText().length() - 1));
                }
            } else {
                this.txtFuncion.setText(this.txtFuncion.getText().substring(0, txtFuncion.getText().length() - 1));
            }
        }
    }//GEN-LAST:event_jButton16ActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (!txtFuncion.getText().equals("")) {
            if (padreMantenedor != null) {
                padreMantenedor.SetNewEditFuncion(txtFuncion.getText());
                padreMantenedor.setEnabledGuardar(true);
                dispose();
            } else {
                this.setCursor(Cursor.WAIT_CURSOR);
                try {
                    FuncionDAO.insertarFuncion(txtFuncion.getText());
                    dispose();
                } catch (DAOException ex) {
                    JOptionPane.showMessageDialog(this, "No se puede Establecer Conexi처n con la Base de Datos", "Error", 0);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "No se puede hacer Rollback", "Error", 0);
                }
                this.setCursor(Cursor.DEFAULT_CURSOR);
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

